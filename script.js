let currentIndex = 0;

function displayHadith(index) {
    const hadithElement = document.getElementById('hadith');
    const tafseerElement = document.getElementById('tafseer');
    hadithElement.textContent = ahadith[index].hadith;
    tafseerElement.textContent = ahadith[index].tafseer;
}

function nextHadith() {
    if (currentIndex < ahadith.length - 1) {
        currentIndex++;
        displayHadith(currentIndex);
    }
}

function prevHadith() {
    if (currentIndex > 0) {
        currentIndex--;
        displayHadith(currentIndex);
    }
}

// عرض الحديث الأول عند تحميل الصفحة
window.onload = () => {
    displayHadith(currentIndex);
};
